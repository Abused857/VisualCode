export class Employee
{
    name: string = "";
    secondName:string = "";
    position:string = "";
    salary:number= 0;
    

    constructor(name:string, secondName:string, position:string, salary:number,)
    {
        this.name = name;
        this.secondName = secondName;
        this.position = position;
        this.salary = salary;

    }

}